package Entidades;

public class Raca {
	
	private long idRaca;
	private String nomeRaca;
	
	public long getIdRaca() {
		return idRaca;
	}
	
	public void setIdRaca(long idRaca) {
		this.idRaca = idRaca;
	}
	
	public String getNomeRaca() {
		return nomeRaca;
	}
	
	public void setNomeRaca(String nomeRaca) {
		this.nomeRaca = nomeRaca;
	}

}
